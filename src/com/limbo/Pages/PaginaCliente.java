    public void loadValues() {
        if(Main.logedUser != null) {
            try {

                Client c = csa.getClientByNumero_Client(Main.logedUser.getNumero_client());

                jTextField1.setText(c.getNom());
                jTextField2.setText(c.getCognom1());
                jTextField3.setText(c.getCognom2());
                jTextField4.setText(c.getEmail());
                jTextField5.setText(c.getUsername());

            }catch (Exception ex) {
                JOptionPane.showMessageDialog(this, ex.getMessage(), "Usuari no trobat", JOptionPane.ERROR_MESSAGE);
            }
        }
    }

