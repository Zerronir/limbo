    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {
        try {

            Client c = new Client();

            c.setNumero_client(Main.logedUser.getNumero_client());

            if(jTextField1.getText().equals("")) {
                throw new Exception("El nom del client no pot estar buit");
            } else {
                c.setNom(jTextField1.getText());
            }

            if(jTextField2.getText().equals("")) {
                throw new Exception("El primer cognom no pot estar buit");
            } else {
                c.setCognom1(jTextField2.getText());
            }

            c.setCognom2(jTextField3.getText());

            if(jTextField4.getText().equals("")) {
                throw new Exception("L'email del client no pot estar buit");
            } else {
                c.setEmail(jTextField4.getText());
            }

            if(jTextField5.getText().equals("")) {
                throw new Exception("El nom d'usuari no pot estar buit");
            } else {
                c.setUsername(jTextField5.getText());
            }

            if(!jPasswordField1.getText().equals(jPasswordField2.getText())) {
                throw new Exception("Les contrasenyes no son iguals");
            } else {
                String pass = csa.hashPassword(jPasswordField1.getText());
                c.setContrasenya(pass);
            }

            csa.update(c);

        }catch (Exception ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage(), "L'usuari no s'ha pogut enregistrar", JOptionPane.ERROR_MESSAGE);
        }
    }

